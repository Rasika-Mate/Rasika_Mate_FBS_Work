# 1. Numeric 
# i.int
x = 10
print(type(x))

# ii.float
x = 3.14
print(type(x))

# iii.complex
x = 10 + 5j
print(type(x))


# 2. Text
# i.String
x = "Python" #Double Quote
print(type(x))

x = 'Python' #Single Quote
print(type(x))


# 3. Sequence Type
# i.list
x = [1,2,'abc',3.14]
print(type(x))
print(x)

# ii.tuple
x = (3,4,'rasika',5.0)
print(type(x))
print(x)

# iii.range
x = (1,10)
print(type(x))
print(x)


# 4. Set Type
# i.set
x = {1,2,'abc',7.8}
print(type(x))
print(x)

# i.frozenset
x = frozenset({1,2,'abc',2.3})
print(type(x))
print(x)


# 5. Mapping
# i.dictionary
x = {'Name':'Rasika', 'Dept':'Comp'}
print(type(x))
print(x)

# 6. Boolean Type
# i. True
x = True
print(type(x))
print(x)

# ii. False
x = False
print(type(x))
print(x)

# 7. None Type
x = None
print(type(x))
print(x)