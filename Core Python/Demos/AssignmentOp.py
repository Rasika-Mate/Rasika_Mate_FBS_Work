# Assignment: Assign values to variable

# 1. =
x = 10
print(x)

# 2. +=
x += 2  # x= x+2
print(x)

# 3. -=
x -= 2  # x= x-2
print(x)

# 4. *=
x *= 2  # x= x*2
print(x)

# 5. /=
x /= 2  # x= x/2
print(x)

# 6. //=
x /= 2  # x= x//2
print(x)

# 7. %=
x /= 2  # x= x%2
print(x)

# 8. **=
x /= 2  # x= x**2
print(x)