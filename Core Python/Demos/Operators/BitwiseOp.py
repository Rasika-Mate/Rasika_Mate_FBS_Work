# Bitwise: Perform bit-by-bit operation (Binary No)

# 1. & (AND): if both bits are 1 then consider 1, otherwise 0
print(13 & 6)

# 2. | (OR): if both bits are 0 then consider 0, otherwise 1
print(13 | 6)

# 3. ^ (XOR): if both bits are diff then consider 1, otherwise 0
print(13 ^ 6)

# 4. ~ (NOT)
print(~10)   # -(10+1) = -11

# 5. << (Left shift)
print(5<<2)

# 6. >> (Right shift)
print(20>>2)