# Relational/Comparision: Compares values & return True or False according to condition

# 1. == (Exact equals to)
print(10 == 10)
print(10 =='10')

# 2. != (Not equals to)
print(10 != 10)
print(10 != '10')

# 3. > (Greater than)
print(10 > 5)
print(10 > 10)

# 4. >= (Greater than (or) equals to)
print(10 >= 5)
print(10 >= 10)
print(10 >= 20)

# 5. < (Less than)
print(10 < 20)
print(20 < 20)

# 6. <= (Less than (or) equals to)
print(10 <= 20)
print(20 <= 20)
print(20 <= 10)