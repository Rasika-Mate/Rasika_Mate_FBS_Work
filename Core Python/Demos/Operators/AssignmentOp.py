# Assignment: Assign values to variable

# 1. =
x = 10
y = 5
print(x)
print(y)

# 2. +=
x += y  # x= x+y
print(x)

# 3. -=
x -= y  # x= x-y
print(x)

# 4. *=
x *= y  # x= x*y
print(x)

# 5. /=
x /= y  # x= x/y
print(x)

# 6. //=
x //= y  # x= x//y
print(x)

# 7. %=
x %= y  # x= x%y
print(x)

# 8. **=
x **= y  # x= x**y
print(x)