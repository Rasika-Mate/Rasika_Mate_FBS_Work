# Logical: used to combine conditional statement

# 1. and (If both operands are True then return True otherwise False)
print(True and True)
print(True and False)

# 2. or (If both operands are False then return False otherwise True)
print(False or False)
print(True or True)

# 3. not (Reverse the result)
print(not True)
print(not False)